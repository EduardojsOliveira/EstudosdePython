#Coerção (Type conversion) seria basicamente converte um tipo para outro tipo
#str, int, float, bool são tipos imutaveis e primitivos, uma vez criada nao da para alterar no progama
print(1 + 1)
print('a' + 'b')
print(int('1'), type(int('1')))
print(int('1') + 1)
print(float('1') + 1)
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')